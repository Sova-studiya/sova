def render_nav():
    items = "\n".join(f'        <li><a href="{href}">{label}</a></li>' for href, label in NAV_ITEMS)
    return items

def render_faq(program):
    blocks = []
    schema_items = []
    for q, a in program["faq"]:
        blocks.append(f'''            <div class="faq-item">
                <button class="faq-question"><span>{q}</span><span class="plus">+</span></button>
                <div class="faq-answer"><p>{a}</p></div>
            </div>''')
        schema_items.append(f'''    {{
      "@type": "Question",
      "name": "{q}",
      "acceptedAnswer": {{ "@type": "Answer", "text": "{a}" }}
    }}''')
    return "\n".join(blocks), ",\n".join(schema_items)

def render_curriculum(items):
    return "\n".join(f'                <li>{item}</li>' for item in items)

def render_about(paragraphs):
    return "\n            ".join(f'<p>{p}</p>' for p in paragraphs)

def render_related(current_slug):
    cards = []
    for p in PROGRAMS:
        if p["slug"] == current_slug:
            continue
        cards.append(f'''            <a class="related-card" href="{p['slug']}.html">
                <img src="img/{p['icon']}" alt="{p['nav_title']}" width="56" height="56" loading="lazy"/>
                <h3>{p['nav_title']}</h3>
            </a>''')
    return "\n".join(cards)

def render_photo_block(slug, photos):
    """Одно фото -> просто <img>. Несколько фото -> карусель."""
    if len(photos) == 1:
        src, alt = photos[0]
        return f'<img src="{src}" alt="{alt}" loading="lazy"/>'

    slides = "\n".join(
        f'                    <img src="{src}" alt="{alt}" loading="lazy"/>' for src, alt in photos
    )
    dots = "\n".join(
        f'                    <button class="carousel-dot{" active" if i == 0 else ""}" data-index="{i}" aria-label="Слайд {i+1}"></button>'
        for i in range(len(photos))
    )
    return f'''<div class="photo-carousel" id="carousel-{slug}" data-count="{len(photos)}">
                <div class="carousel-track">
{slides}
                </div>
                <button class="carousel-btn carousel-prev" aria-label="Предыдущее фото">&#10094;</button>
                <button class="carousel-btn carousel-next" aria-label="Следующее фото">&#10095;</button>
                <div class="carousel-dots">
{dots}
                </div>
            </div>'''

